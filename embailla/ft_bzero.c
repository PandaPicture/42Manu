#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t var;

	var = 0;
	if (n == 0)
		return ;
	else
	{
		while (var <= n)
		{
			((char*)s)[var] = 0;
			var++;
		}
	}
	return ;
}
